<h2 class="font-weight-bold"style="font-size:24px">How FASTag works?</h2>

<br><span>

FASTag is a radio frequency identification (RFID) system that functions in a similar way to other RFID systems. A RFID-enabled sticker is attached to a vehicle's windshield, and a reader at the toll booth may scan this card and process the payment electronically and automatically.
<br>

