<h2 class="font-weight-bold"style="font-size:24px">What is FASTag?</h2>

<br><span>

FASTag is a kind of tag which is placed in the front side mirror of the vehicle for automatic deduction of toll payment in the toll plaza without waiting for any cash transaction. </span>

<br>

<br><span>

This is done through the use of <font color="red">Radio Frequency Identification (RFID)</font> technology on toll plaza that automatically detect the FASTag on the Widescreen of vehicles passing the toll. This tag is connected with a bank account of a person who owns the vehicle for payment deduction.</span>

<br>

<br>

<h3 class="font-weight-bold"style="font-size:24px">How FASTag works?</h3>

<br><span>

The FASTtag system collects electronic payment through the tag placed in front of the vehicle mirror because that tag is connected with a bank account of the person holding the vehicle. A FASTag account is created in your bank for the purpose of this. In order to get your account active then you need to regularly recharge your FASTag account. 

<br>

<br>

In toll plaza, there is a system integrated with radio frequency identification technology that automatically detects the dag and collects the payment without the involvement of cash transaction.

<br>

<br>

<h3 class="font-weight-bold"style="font-size:24px">Is it mandatory to have a FASTag?</h3>

<br><span>

Yes, it is mandatory for people who are running their personal or commercial vehicles on the national highways. If you are caught without a <a href="https://tolltaxes.com/" target="_blank">FASTag</a> then you have to pay a heavy fine.</span>

