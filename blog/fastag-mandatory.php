With this decision by the Union Ministry of Road Transport and Highways (MoRTH), FASTag will be mandatory for those owning four-wheelers purchased before 1 December 2017. The Ministry has declared that it should be mandatory for all four-wheelers, irrespective of the year of manufacture and age.<br><br>
 
FASTag has been mandatory for the registration of new four-wheelers since December 1, 2017, as per the Central Motor Vehicles Regulations, 1989, and is supplied by vehicle manufacturers/dealers.<br><br>
 
In the face of owners' unwillingness to go for electronic means to pay toll fees, the notification makes it mandatory for all four-wheelers. Last year, with all but one lane devoted to electronic toll collection, the compulsory introduction of FASTags at highway toll plazas also failed to produce the desired result.<br><br>
 
To ensure enforcement, MoRTH has made vehicle insurance mandatory for FASTags. Only if the vehicle has a current FASTag can new third-party vehicle insurance from April 1, 2021, be provided. From now on, details of the FASTag ID will be reported on Form 51 while seeking a new third-party insurance policy (certificate of insurance).<br><br>
 
As in the previous drive, MoRTH agreed in the next few days to make FASTag available on multiple channels. It is expected that banks and online mechanisms will make them available so that owners can buy and affix them to their cars.<br><br>
 
MoRTH's flagship project, the National Electronic Toll Collection (NETC) program, utilizes passive radio frequency identification (RFID) technology for user fee collection. FASTag, a prepaid sticker, allows toll charges to be automatically deducted and allows the vehicle to drive through the toll plaza without stopping for the cash transaction.<br><br>
 
